package org.wso2.carbon.identity.api.server.credential.management.v1;

import io.swagger.annotations.ApiModel;
import javax.validation.constraints.*;

/**
 * The type of the credential.
 **/
import javax.xml.bind.annotation.XmlType;
import javax.xml.bind.annotation.XmlEnum;
import javax.xml.bind.annotation.XmlEnumValue;

@XmlType(name="")
@XmlEnum(String.class)
public enum CredentialType {

    @XmlEnumValue("passkey") PASSKEY(String.valueOf("passkey")), @XmlEnumValue("push-auth") PUSH_AUTH(String.valueOf("push-auth"));


    private String value;

    CredentialType(String v) {
        value = v;
    }

    public String value() {
        return value;
    }

    @Override
    public String toString() {
        return String.valueOf(value);
    }

    public static CredentialType fromValue(String value) {
        for (CredentialType b : CredentialType.values()) {
            if (b.value.equals(value)) {
                return b;
            }
        }
        throw new IllegalArgumentException("Unexpected value '" + value + "'");
    }
}



